`timescale 1ns / 1ps
// ============================================================================
// Module: stage2_wallace
// Description: 8x8 无符号乘法器 - 第二级组合逻辑 (6行压2行 华莱士树)
// Architecture: 符号对齐 -> 3 级 CSA 压缩网络 -> 吐出 Sum 和 Carry
// ============================================================================

// ----------------------------------------------------------------------------
// 子模块：16位 3-to-2 保留进位压缩器 (Carry-Save Adder, CSA)
// ----------------------------------------------------------------------------
module csa_16b (
    input  wire[15:0] in1,
    input  wire [15:0] in2,
    input  wire [15:0] in3,
    output wire [15:0] sum,
    output wire [15:0] carry
);
    // Sum：本位异或和 (没有进位传播)
    assign sum   = in1 ^ in2 ^ in3;
    // Carry：多数表决逻辑。因为是进位，所以权重高 1，必须左移 1 位！
    assign carry = ((in1 & in2) | (in2 & in3) | (in1 & in3)) << 1; 
endmodule

// ----------------------------------------------------------------------------
// 主模块：Stage 2 华莱士压缩树
// ----------------------------------------------------------------------------
module stage2_wallace (
    // 接收 Stage 1 送来的 5 行部分积 (10位)
    input  wire [9:0] pp0,
    input  wire[9:0] pp1,
    input  wire [9:0] pp2,
    input  wire[9:0] pp3,
    input  wire [9:0] pp4,
    // 接收 Stage 1 送来的 5 个负数补偿位 (+1)
    input  wire       c0,
    input  wire       c1,
    input  wire       c2,
    input  wire       c3,
    input  wire       c4,

    // 吐出给 Stage 3 的最终两行结果 (16位)
    output wire [15:0] S_out,
    output wire[15:0] C_out
);

    // ========================================================================
    // 1. 符号位扩展与移位对齐 (Sign Extension & Alignment)
    // 目标：把长短不一、权重不同的 10 位数据，全部对齐到 16 位的坐标系里
    // ========================================================================
    
    // P0: 权重 2^0，不移位。需补充 16-10 = 6 位符号位
    wire[15:0] P0 = { {6{pp0[9]}}, pp0 };
    
    // P1: 权重 2^2，左移 2 位。需补充 16-10-2 = 4 位符号位
    wire [15:0] P1 = { {4{pp1[9]}}, pp1, 2'b00 };
    
    // P2: 权重 2^4，左移 4 位。需补充 16-10-4 = 2 位符号位
    wire [15:0] P2 = { {2{pp2[9]}}, pp2, 4'b0000 };
    
    // P3: 权重 2^6，左移 6 位。10+6 = 16，刚好满位，无需符号扩展
    wire [15:0] P3 = { pp3, 6'b000000 };
    
    // P4: 权重 2^8，左移 8 位。
    // 【物理真理】：PP4 是无符号特有的补偿位，它只可能是 0 或 +A，绝对不会是负数！
    // 所以它的最高位绝对是 0，我们直接截取低 8 位，并在右边补 8 个 0 即可。
    wire [15:0] P4 = { pp4[7:0], 8'b00000000 }; 
    
    // C_row: 把所有的负数补偿位拼成最后一行。
    // c0权重2^0, c1权重2^2, c2权重2^4, c3权重2^6, c4绝对是0。
    // 拼接成 16 位宽的数组 (9个0 + c3 + 0 + c2 + 0 + c1 + 0 + c0 = 16位)
    wire [15:0] C_row = {9'd0, c3, 1'b0, c2, 1'b0, c1, 1'b0, c0};

    // ========================================================================
    // 2. Wallace 树压缩网络 (6行 压 2行)
    // ========================================================================
    
    // --- 树的第一层 (Level 1) ---
    // 并行吃掉 6 行，吐出 4 行
    wire [15:0] S1_0, C1_0;
    wire [15:0] S1_1, C1_1;
    
    // 压缩 P0, P1, P2
    csa_16b u_csa_L1_0 (
        .in1(P0), .in2(P1), .in3(P2), 
        .sum(S1_0), .carry(C1_0)
    );
    
    // 并行压缩 P3, P4, C_row
    csa_16b u_csa_L1_1 (
        .in1(P3), .in2(P4), .in3(C_row), 
        .sum(S1_1), .carry(C1_1)
    );

    // --- 树的第二层 (Level 2) ---
    // 此时剩下 4 行：S1_0, C1_0, S1_1, C1_1
    // 我们先压前 3 行，让 C1_1 在旁边歇一会儿
    wire[15:0] S2, C2;
    
    csa_16b u_csa_L2 (
        .in1(S1_0), .in2(C1_0), .in3(S1_1), 
        .sum(S2), .carry(C2)
    );

    // --- 树的第三层 (Level 3) ---
    // 此时剩下 3 行：S2, C2, 以及刚才歇着的 C1_1
    // 最后一级压缩，完美变成 2 行！
    csa_16b u_csa_L3 (
        .in1(S2), .in2(C2), .in3(C1_1), 
        .sum(S_out), .carry(C_out) // 直接连到输出引脚
    );

endmodule