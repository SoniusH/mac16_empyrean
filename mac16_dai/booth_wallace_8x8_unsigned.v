`timescale 1ns / 1ps
// ============================================================================
// Module: booth_wallace_8x8_unsigned
// Description: 纯手工 8x8 无符号乘法器 (Radix-4 Booth + 6-to-2 Wallace Tree)
// ============================================================================

// ----------------------------------------------------------------------------
// 子模块：16位 3-to-2 保留进位压缩器 (CSA)
// ----------------------------------------------------------------------------
module csa_16b (
    input  wire [15:0] in1,
    input  wire [15:0] in2,
    input  wire [15:0] in3,
    output wire [15:0] sum,
    output wire [15:0] carry
);
    assign sum   = in1 ^ in2 ^ in3;
    // 进位必须左移 1 位，这是 CSA 的核心灵魂
    assign carry = ((in1 & in2) | (in2 & in3) | (in1 & in3)) << 1; 
endmodule

// ----------------------------------------------------------------------------
// 主模块：8x8 无符号乘法器
// ----------------------------------------------------------------------------
module booth_wallace_8x8_unsigned (
    input  wire [7:0] A,
    input  wire [7:0] B,
    output wire [15:0] P
);

    // ========================================================================
    // 阶段 1：无符号数的 Booth 编码准备 (极其核心的防错处理)
    // 8位无符号数 B 必须在高位补 2 个 0，低位补 1 个 0，扩展为 11 位
    // 这样才能安全地拆分成 5 组 Booth 编码！
    // ========================================================================
    wire [10:0] B_pad = {2'b00, B, 1'b0};

    wire [2:0] code0 = B_pad[2:0];
    wire [2:0] code1 = B_pad[4:2];
    wire [2:0] code2 = B_pad[6:4];
    wire [2:0] code3 = B_pad[8:6];
    wire[2:0] code4 = B_pad[10:8]; // 这是无符号独有的第 5 组 (只可能是 000 或 001)

    // ========================================================================
    // 阶段 2：预处理倍数 (扩展到 10 位防止溢出)
    // ========================================================================
    wire[9:0] A_pos1 = {2'b00, A};       // +A
    wire [9:0] A_pos2 = {1'b0, A, 1'b0};  // +2A
    wire [9:0] A_neg1 = ~A_pos1;          // -A (反码，需补偿+1)
    wire [9:0] A_neg2 = ~A_pos2;          // -2A (反码，需补偿+1)

    reg [9:0] pp0, pp1, pp2, pp3, pp4;
    reg c0, c1, c2, c3, c4;

    // ========================================================================
    // 阶段 3：生成 5 行部分积
    // ========================================================================
    always @(*) begin
        // --- PP0 ---
        case(code0)
            3'b001, 3'b010: begin pp0 = A_pos1; c0 = 1'b0; end
            3'b011:         begin pp0 = A_pos2; c0 = 1'b0; end
            3'b100:         begin pp0 = A_neg2; c0 = 1'b1; end
            3'b101, 3'b110: begin pp0 = A_neg1; c0 = 1'b1; end
            default:        begin pp0 = 10'd0;  c0 = 1'b0; end
        endcase
        // --- PP1 ---
        case(code1)
            3'b001, 3'b010: begin pp1 = A_pos1; c1 = 1'b0; end
            3'b011:         begin pp1 = A_pos2; c1 = 1'b0; end
            3'b100:         begin pp1 = A_neg2; c1 = 1'b1; end
            3'b101, 3'b110: begin pp1 = A_neg1; c1 = 1'b1; end
            default:        begin pp1 = 10'd0;  c1 = 1'b0; end
        endcase
        // --- PP2 ---
        case(code2)
            3'b001, 3'b010: begin pp2 = A_pos1; c2 = 1'b0; end
            3'b011:         begin pp2 = A_pos2; c2 = 1'b0; end
            3'b100:         begin pp2 = A_neg2; c2 = 1'b1; end
            3'b101, 3'b110: begin pp2 = A_neg1; c2 = 1'b1; end
            default:        begin pp2 = 10'd0;  c2 = 1'b0; end
        endcase
        // --- PP3 ---
        case(code3)
            3'b001, 3'b010: begin pp3 = A_pos1; c3 = 1'b0; end
            3'b011:         begin pp3 = A_pos2; c3 = 1'b0; end
            3'b100:         begin pp3 = A_neg2; c3 = 1'b1; end
            3'b101, 3'b110: begin pp3 = A_neg1; c3 = 1'b1; end
            default:        begin pp3 = 10'd0;  c3 = 1'b0; end
        endcase
        // --- PP4 (无符号独有补偿行：绝不会出现负数！) ---
        case(code4)
            3'b001, 3'b010: begin pp4 = A_pos1; c4 = 1'b0; end
            default:        begin pp4 = 10'd0;  c4 = 1'b0; end
        endcase
    end

    // ========================================================================
    // 阶段 4：符号位扩展与移位对齐 (对齐到 16 位)
    // ========================================================================
    wire [15:0] P0 = { {6{pp0[9]}}, pp0 };
    wire [15:0] P1 = { {4{pp1[9]}}, pp1, 2'b00 };
    wire [15:0] P2 = { {2{pp2[9]}}, pp2, 4'b0000 };
    wire [15:0] P3 = { pp3, 6'b000000 };
    // PP4 绝不会是负数，高位无脑补 0 即可
    wire[15:0] P4 = { pp4[7:0], 8'b00000000 }; 
    
    // 把负数的 +1 补偿位收集成一行
    wire[15:0] C_row = {9'd0, c3, 1'b0, c2, 1'b0, c1, 1'b0, c0};

    // ========================================================================
    // 阶段 5：Wallace Tree 压缩 (6 行压成 2 行)
    // 我们有 6 行输入：P0, P1, P2, P3, P4, C_row
    // ========================================================================
    
    // 第一层：两组并行的 3-to-2 压缩
    wire [15:0] S1_0, C1_0;
    csa_16b u_csa_L1_0 (.in1(P0), .in2(P1), .in3(P2), .sum(S1_0), .carry(C1_0));
    
    wire[15:0] S1_1, C1_1;
    csa_16b u_csa_L1_1 (.in1(P3), .in2(P4), .in3(C_row), .sum(S1_1), .carry(C1_1));

    // 第二层：处理 S1_0, C1_0, S1_1 (还剩 C1_1 在旁边等)
    wire [15:0] S2, C2;
    csa_16b u_csa_L2 (.in1(S1_0), .in2(C1_0), .in3(S1_1), .sum(S2), .carry(C2));

    // 第三层：处理 S2, C2, C1_1 (压至最后 2 行！)
    wire [15:0] S3, C3;
    csa_16b u_csa_L3 (.in1(S2), .in2(C2), .in3(C1_1), .sum(S3), .carry(C3));

    // ========================================================================
    // 阶段 6：最终快速加法器 (CPA)
    // ========================================================================
    assign P = S3 + C3;

endmodule